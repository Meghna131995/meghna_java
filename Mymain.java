import java.sql.SQLOutput;

public class Mymain {
    public static void main(String[] args){
        int i=10;
        int k=5;
        System.out.println(i+k); //Addition
        System.out.println(i-k); //Subtraction
        System.out.println(i*k); //Multiplication
        System.out.println(i/k); //Division with Quotient as result
        System.out.println(i%k); //Division with Remainder as result

        float a=9.1f;
        float b=2.3f;
        System.out.println(a+b); //Float Addition

        String str= "Printing string";
        System.out.println(str);

    }

}
